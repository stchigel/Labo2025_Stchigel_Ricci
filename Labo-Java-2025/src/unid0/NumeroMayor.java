public class NumeroMayor {
    public static void main(String[] args) {
        int Romero_uno=4;
        int nomero_dos=5;
        if(Romero_uno>nomero_dos){
            System.out.println("El numero 1 es mas grande que el 2");
        }
        else if(nomero_dos>Romero_uno){
            System.out.println("El numero 2 es mas grande que el 1");
        }
        else{
            System.out.println("El numero 1 tiene el mismo valor que el 2");
        }
    }
}
